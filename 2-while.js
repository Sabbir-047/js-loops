let num = 0;
while (num < 5) {
  console.log(num);
  num++;
}

// range
let number = 10;
while (number <= 20) {
  console.log(number);
  number++;
}

// range sum
let numSum = 20;
let sum = 0;
while (numSum <= 30) {
  sum += numSum;
  console.log("num:", numSum, "sum:", sum);
  numSum++;
}

// even num
let evenNum = 0;
while (evenNum <= 20) {
  console.log("even:", evenNum);
  evenNum += 2;
}
